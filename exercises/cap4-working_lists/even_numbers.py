"""La funcion range() puede tomar un tercer parametro que indica el incremento entre cada numero en la secuencia."""

even_numbers = list(range(2, 11, 2))
print(even_numbers)
# This code creates a list of even numbers from 2 to 20.
# The range function starts at 2, ends before 11, and increments by 2.
# Output:
# [2, 4, 6, 8, 10,]
# You can modify the parameters to generate different sequences.

